# Re-loading or using a copy for categorical plots if necessary,
# or plotting specific dummy columns.

# 2. Correlation heatmap
plt.figure(figsize=(12, 8))
# Added numeric_only=True to fix the ValueError with string columns
sns.heatmap(df.corr(numeric_only=True), annot=True, cmap='coolwarm', fmt='.2f')
plt.title('Correlation Heatmap')
plt.show()

# 3. Attendance vs Score
# Note: 'Attendance (%)' is the correct column name based on previous exploration
attendance_col = 'Attendance (%)'
if attendance_col in df.columns and 'Total_Score' in df.columns:
    plt.figure(figsize=(10, 6))
    sns.scatterplot(x=attendance_col, y='Total_Score', data=df)
    plt.title('Attendance vs Total Score')
    plt.show()

